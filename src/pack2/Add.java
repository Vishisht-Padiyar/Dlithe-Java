package pack2;

public class Add {
	int add(int a, int b) {
		System.out.println("Int o/p = " + (a+b));
		return a+b;
	}
	
	int add(int a, int b, int c) {
		System.out.println("Int o/p = " + (a+b+c));
		return a+b+c;
	}
	float add(float a, float b) {
		System.out.println("Float o/p = " + (a+b));
		return a+b;
	}

}
